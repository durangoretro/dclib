.include "durango_constants.inc"
.PC02

.export coords2mem
.export readchar
.export keyrow1

.proc coords2mem: near
    ; Calculate Y coord
    STZ VMEM_POINTER
    LDA Y_COORD
    LSR
    ROR VMEM_POINTER
    LSR
    ROR VMEM_POINTER
    ADC #$60
    STA VMEM_POINTER+1
    ; Calculate X coord
    LDA X_COORD
    LSR
    CLC
    ADC VMEM_POINTER
    STA VMEM_POINTER
    BCC skip_upper
    INC VMEM_POINTER+1
    skip_upper:
    RTS
.endproc

.proc readchar: near
    LDX KEYBOARD_CACHE
    LDA keyrow1,X
    BNE end
    
    LDX KEYBOARD_CACHE+1
    LDA keyrow2,X
    BNE end
    
    end:
    RTS
.endproc


